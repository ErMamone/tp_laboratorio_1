/** \brief La funcion realiza la suma de los numeros dados por parametros
 *
 * \param  x variable que guarda el primer numero
 * \param  y variable que guarda el primer numero
 *
 * \return Retorna la suma de los dos numeros
 */
float suma (float x, float y);


/** \brief La funcion realiza la resta de los numeros dados por parametros
 *
 * \param  x variable que guarda el primer numero
 * \param  y variable que guarda el primer numero
 *
 * \return Retorna la resta de los dos numeros
 */
float resta (float x, float y);


/** \brief La funcion realiza la divison de los numeros dados por parametros
 *
 * \param x variable que guarda el primer numero
 * \param y variable que guarda el primer numero
 *
 * \return Retorna la division del numero X sobre el numero Y
 */
float dividido (float x, float y);


/** \brief La funcion realiza la multiplicacion de los numeros dados por parametros
 *
 * \param  x variable que guarda el primer numero
 * \param  y variable que guarda el primer numero
 *
 * \return Retorna la multiplicacion de los dos numeros dados
 */
float multiplicar (float x, float y);


/** \brief La funcion realiza la factorizacion del numero dado, el mismo no puede ser 0 o negativo
 *
 * \param  x variable que guarda el primer numero
 *
 * \return Retorna la factorizacion del numero dado
 */
float factorizar (float x);
